    {
        //
        $bookings = Booking::all()->map(function($booking){
            $itinerary_seat = DB::table('itinerary_seat')
            ->select('itinerary_id', 'seat_id')
            ->where('booking_id', $booking->id)
            ->get();
            $booking->itinerary = \App\Itinerary::find($itinerary_seat->first()->itinerary_id);
            $booking->seats = \App\Seat::find($itinerary_seat->pluck('seat_id')->toArray());
            return $booking;
        });
        return view('backend.booking.index', compact('bookings'));
    }

